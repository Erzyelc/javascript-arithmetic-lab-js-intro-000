function add(a,b){
  let result = a + b;
  return result;
}

function subtract(a, b){
  let result2 = a - b;
  return result2;
}

function multiply(a, b){
  let result3 = a * b;
  return result3;
}

function divide(a, b){
  let result4 = a / b;
  return result4;
}

function increment(n){
  let incremented = n++;
  return incremented;
}

function decrement(n){
  let decremented = n--;
  return decremented;
}
